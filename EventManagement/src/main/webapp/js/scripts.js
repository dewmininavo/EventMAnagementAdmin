// Function to fetch event count from the server
function fetchEventCount() {
  fetch('/api/event-count') // Replace with your actual API endpoint
    .then(response => response.json())
    .then(data => {
      // Update the event count display
      document.getElementById('event-count').innerText = data.count;

      // Update bar chart data with new values (for example)
      barChart.updateSeries([
        {
          data: [data.count, 8, 6, 4], // Adjust this to fit your data structure
          name: 'Products',
        },
      ]);
    })
    .catch(error => console.error('Error fetching event count:', error));
}

// Fetch event count on initial load
fetchEventCount();

// Optional: Fetch event count every 60 seconds
setInterval(fetchEventCount, 60000); // 60000 ms = 1 minute
